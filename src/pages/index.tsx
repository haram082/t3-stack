import Head from "next/head";
import Link from "next/link";
import { api } from "~/utils/api";
import { SignInButton } from "@clerk/nextjs";
import { SignOutButton } from "@clerk/nextjs";
import { useUser } from "@clerk/nextjs";


const CreatePostWizard =()=>{
  const{user} = useUser()
  if(!user) return null
  return(
    <div className="flex gap-4 w-full">
      <img src={user.profileImageUrl} alt="pfp"  className="h-16 w-16 rounded-full"/>
      <input type="text" placeholder="Create a Post" className="bg-transparent outline-none " />
      </div>
  )
}

export default function Home() {
  const user = useUser()
  const {data, isLoading} = api.posts.getAll.useQuery()
    
  if (isLoading) {
    return <div>Loading...</div>
  }
  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex justify-center h-screen">
        <div className="h-full w-full md:max-w-2xl border-x border-slate-400">
    
      <div className="border-b border-slate-400 p-4 flex justify-between">
        {!user.isSignedIn && <SignInButton/>}
      {user.isSignedIn && <CreatePostWizard />} 
      {user.isSignedIn && <SignOutButton />} </div>


      <ul className="flex flex-col ">
        {data?.map((post) => (
          <li key={post.id} className=" border-b border-slate-400 p-8">
              <div>{post.content}</div>
          </li>
        ))}
      </ul> 
      </div> 
      </main>
    </>
  );
}
